package coreJava;

public class SwitchStatement {

	public static void main(String[] args) {
		
		int score = 80;
		
		switch(score)
		{
		case 70:
			System.out.println("Result is very good");
			break;
			
		case 60:
			System.out.println("Result is good");
			break;
			
		case 50:
			System.out.println("Result is ok");
			break;
			
		case 40:
			System.out.println("Just pass");
			break;
			
			default:
				System.out.println("Grade is not define");
				break;
			
		
			
			
		}
		
		
		

	}

}
