package coreJava;

public class SingleArray {

	public static void main(String[] args) {
		
		int []singleArray = {10,32,20,45,43,67,234,223,78,98,44,100,376,987,22};
		
		System.out.println("Total array length is "+singleArray.length);
		
		System.out.println("Index no 10 value is "+singleArray[10]);
		
		for(int a=0;a<singleArray.length;a++)
		{
			System.out.println("Total value of array is "+singleArray[a]);
		}

	}

}
